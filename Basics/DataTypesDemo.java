package Basics;
public class DataTypesDemo {
public static void main(String[] args) {
byte by = 100;
short sh = 30000;
int i = 1_000_000;
long l = 123456789L;
float f = 3.14f;
double d = 2.71828;
char c = 'A';
boolean bool = false;


System.out.println(by + ", " + sh + ", " + i + ", " + l);
System.out.println(f + ", " + d + ", " + c + ", " + bool);
}
}